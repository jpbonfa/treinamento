package exemplo04;

public class IncrementoDecremento {

	public static void main(String[] args) {
		int numero = 1;

		System.out.println(++numero);

		numero = 1;
		System.out.println(numero++);
		System.out.println(numero);
		System.out.println(++numero);
		System.out.println(numero++); 
		System.out.println(numero++);
		System.out.println(numero);
		System.out.println(numero--);
		System.out.println(numero--);
		System.out.println(numero--);
		
	
	}

}
